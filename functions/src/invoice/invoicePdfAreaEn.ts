/** Same normalization as app `ConversationalSearchService._code` on English labels. */
function areaCodeKeyFromEnLabel(en: string): string {
  return en
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "_")
    .replace(/-/g, "_")
    .replace(/[^a-z0-9_]/g, "")
    .replace(/_+/g, "_")
    .replace(/^_+|_+$/g, "");
}

const areaCodeToEnLabel: Record<string, string> = (() => {
  const m: Record<string, string> = {};
  for (const en of Object.values(areaArToEn)) {
    const k = areaCodeKeyFromEnLabel(en);
    if (k && m[k] === undefined) {
      m[k] = en;
    }
  }
  return m;
})();

function hasArabicScript(s: string): boolean {
  return /[\u0600-\u06FF]/.test(s);
}

/** Heuristic: Firestore doc id or opaque token (not a normal place name). */
function looksLikeOpaqueId(s: string): boolean {
  const t = s.trim();
  if (t.length < 6 || t.length > 28) return false;
  if (/[\s\u0600-\u06FF.]/.test(t)) return false;
  if (!/^[a-zA-Z0-9_-]+$/.test(t)) return false;
  if (/\d/.test(t)) return true;
  if (t.length >= 20) return true;
  return false;
}

/**
 * Human-readable English area for the invoice. Never returns Arabic.
 * Unknown opaque ids map to "Kuwait".
 */
export function areaDisplayEnglish(raw: string): string {
  const t = raw.trim();
  if (!t) return "Kuwait";
  const lowerKuwait = t.toLowerCase();
  if (t === "الكويت" || lowerKuwait === "kuwait") {
    return "Kuwait";
  }

  const fromArArea = areaArToEn[t];
  if (fromArArea) return fromArArea;

  const fromArGov = governorateArToEn[t];
  if (fromArGov) return fromArGov;

  const codeKey = areaCodeKeyFromEnLabel(t);
  if (codeKey && areaCodeToEnLabel[codeKey]) {
    return areaCodeToEnLabel[codeKey];
  }

  if (hasArabicScript(t)) {
    return "Kuwait";
  }

  if (looksLikeOpaqueId(t)) {
    return "Kuwait";
  }

  return t;
}
